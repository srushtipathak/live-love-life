          <h1>Does suicide mean the person has/had a mental illness?</h1>
          <p>Not all people who have thoughts of suicide or attempt suicide have a mental illness. And not all mental illnesses make a person vulnerable to suicide. There are several biological, psychological and social factors that may make a person vulnerable to suicide. Suicide is a complex phenomenon resulting from a combination of several factors. Mental illness may or may not be one of them. While suicide is an indicator of intense emotional distress, it is not necessarily an indicator of a mental illness.
<br>
Is a suicide attempt a sign of the person’s weakness or inability to handle their problems?
It’s a common belief that suicide is a sign of the person’s weakness or their lack of courage in facing their challenges. A person doesn’t simply opt to have thoughts of suicide. They may have thoughts of suicide due to an interplay of several factors – biological, psychological and social. Thoughts of suicide are usually a symptom of extreme distress and can overwhelm an individual irrespective of how strong they are.</p>
