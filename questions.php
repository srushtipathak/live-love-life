          <h1>Is anyone with depression at risk of suicide?</h1>
          <p>People with mood disorders such as depression may biologically be more at risk of suicide than people who don’t. However, the social component matters too: if a person who has depression has a loving and supportive family, and a workplace or community spaces where they experience acceptance and support, they may be able to seek support in time and tide over their emotional distress. The existence of a mental illness (or lack of it) is not the only factor that determines a person's vulnerability.
<br>
If someone is harming themselves, does it mean they’re at risk of suicide?
While some people with thoughts of suicide may harm themselves, not everyone who harms themselves is doing so with an intent to end their life. This is being recognized across the world today, with the DSM-5’s new classification called Nonsuicidal Self Injury disorder (NSSI). Self harm here is used mostly as a coping mechanism or a distraction, rather than the end in itself. People who self harm may harm themselves for months or years without ever attempting to end their life. This said, self-harm can be one of the risk factors for suicide, and should be monitored closely</p>
