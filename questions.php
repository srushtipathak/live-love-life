          <p>Any individual can experience thoughts of suicide, irrespective of their age, personality traits, accomplishments or social status. Certain factors such as the existence of a mental illness, marginalization, and exposure to socio-cultural stressors may increase an individual’s vulnerability. Research shows that in India, the rate of suicide is highest in the 15-29 age group with women in that age group being more vulnerable than men. That said, there is no age group that is immune.</p>
