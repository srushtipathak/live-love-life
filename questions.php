          <h1>But why don’t they reach out?</h1>
          <p>A person who has thoughts of suicide is likely to have an intense experience of loneliness, helplessness and hopelessness, which makes it hard for them to reach out for help. They may also fear judgment by others. They often attempt to cope with their problems by engaging in behaviors that may not easily be seen as a cry for help – withdrawing from hobbies, work, family or friends, suddenly becoming dependent on substances or by broaching the subject in a roundabout way with people they know. Some people may choose not to reach out to the ones closest to them because they’re worried about how they will take it, or fear that the person they reach out to won’t understand what they’re going through. The existing misconceptions and stigma around suicide and mental illness can make it more difficult to reach out.</p>
